package pth;

public class Cell {
	private boolean s;
	private boolean u;
	private boolean z;
	private boolean v;
	
	public boolean isS() {
		return s;
	}
	public void setS(boolean s) {
		this.s = s;
	}
	public boolean isU() {
		return u;
	}
	public void setU(boolean u) {
		this.u = u;
	}
	public boolean isZ() {
		return z;
	}
	public void setZ(boolean z) {
		this.z = z;
	}
	public boolean isV() {
		return v;
	}
	public void setV(boolean v) {
		this.v = v;
	}

	

}
